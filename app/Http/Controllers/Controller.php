<?php

namespace App\Http\Controllers;

abstract class Controller
{
    protected function handleValidation(callable $callback)
    {
        try {
            return $callback();
        } catch (\Illuminate\Validation\ValidationException $e) {
            return response()->json([
                'success' => false,
                'errors' => $e->errors()
            ], 422);
        } catch (\Exception $e) {
            return response()->json([
                'success' => false,
                'message' => $e->getMessage()
            ], 500);
        }
    }
}
